    private void fillToTable() {//Duyệt list => Đổ dữ liệu lên bảng
        model.setRowCount(0);
        for (Employee x : lstNhanVien) {
            model.addRow(new Object[]{x.getMaNV(), x.getHoTen(), x.getTuoi(), x.getNgoaiNgu(), x.getTuoiLaoDong(x.getTuoi())});
        }
    }

    private void showDetail() {//Hiển thị phần tử hiện tại lên form
        Employee e = lstNhanVien.get(index);
        txtMaNV.setText(e.getMaNV());
        txtHoTen.setText(e.getHoTen());
        txtTuoi.setText(e.getTuoi() + "");
        txtNgoaiNgu.setText(e.getNgoaiNgu());
        tblNhanVien.setRowSelectionInterval(index, index);
    }

    private void clearForm() {//Xóa trắng dữ liệu tại các Text
        txtMaNV.setText("");
        txtHoTen.setText("");
        txtTuoi.setText("");
        txtNgoaiNgu.setText("");
        index = -1;
    }

