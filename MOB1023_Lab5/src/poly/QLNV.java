    private void btnGhiFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiFileActionPerformed
        //Ghi ArrayList< Empolyee > vào File + thông báo thành công
        try {
            FileOutputStream fos = new FileOutputStream("a.txt");
            ObjectOutputStream oos = new ObjectOutputStream(fos);
            oos.writeObject(lstNhanVien);
            oos.flush();
            fos.flush();
            oos.close();
            fos.close();
            JOptionPane.showMessageDialog(this, "Ghi thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi ghi file");
        }
    }//GEN-LAST:event_btnGhiFileActionPerformed

    private void btnDocFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDocFileActionPerformed
//        Đọc File: Đọc dữ liệu từ file => Đổ ArrayList< Empolyee >. 
//Sau khi đọc:
//-	Nếu list không có dữ liệu: Để trống các text
//-	Nếu List có dữ liệu: Hiển thị phần tử đầu tiên 
        //+ Hiển thị thông tin Empolyee lên Table
        try {
            FileInputStream fis = new FileInputStream("a.txt");
            ObjectInputStream ois = new ObjectInputStream(fis);
            lstNhanVien = (ArrayList<Employee>) ois.readObject();
            ois.close();
            fis.close();
            //Xử lý sau khi đọc
            if (lstNhanVien.size() > 0) {
                fillToTable();
                index = 0;
                showDetail();
            } else {
                clearForm();
            }
            JOptionPane.showMessageDialog(this, "Đọc thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi đọc file");
        }

    }//GEN-LAST:event_btnDocFileActionPerformed

    private void btnGhiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGhiActionPerformed
        //Ghi: 
//•	Kiểm tra rỗng, kiểm tra tuổi là số, tuổi 15-65 
//•	Lấy dữ liệu trên form để thêm 1 phần tử: Save vào ArrayList, Save vào JTable

        try {
            //Kiểm tra rỗng
            if (txtMaNV.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Phải nhập Manv");
                txtMaNV.requestFocus();
                return;
            }
            //Tương tự kiểm tra rỗng cho hoten, tuoi, ngoaiNgu
            //Kiểm tra là số
            int tuoi;
            try {
                tuoi = Integer.parseInt(txtTuoi.getText());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Tuổi phải là số");
                txtTuoi.requestFocus();
                return;
            }
            //Kiểm tra miền tuổi 15-65 
            if (tuoi < 15 || tuoi > 65) {
                JOptionPane.showMessageDialog(this, "Tuổi phải 15-65");
                txtTuoi.requestFocus();
                return;
            }
            //Lấy dữ liệu trên form
            String manv = txtMaNV.getText();
            String hoTen = txtHoTen.getText();
            String ngoaiNgu = txtNgoaiNgu.getText();
            //Thêm vào list
            Employee e=new Employee(manv, hoTen, ngoaiNgu, tuoi);
            lstNhanVien.add(e);
            //Thêm vào model <=> fillToTable
            model.addRow(new Object[]{manv,hoTen,tuoi,ngoaiNgu
                    ,e.getTuoiLaoDong(tuoi)});
            //Xử lý sau khi thêm
            index= lstNhanVien.size()-1;
            showDetail();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Lỗi nút ghi");
        }
    }//GEN-LAST:event_btnGhiActionPerformed

