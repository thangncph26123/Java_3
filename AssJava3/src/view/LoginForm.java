    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            Object o = userServiceImpl.findUsers(txtUsername.getText(), txtPassword.getText());
            if(o == null){
                return;
            }else if(o instanceof String){
                lblError.setText(o.toString());
                txtUsername.requestFocus();
            }else{
                this.dispose();
                new Home((Users) o).setVisible(true);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

