    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        try {
            Users user = userService.findUser(txtUsername.getText().trim(), txtPassword.getText().trim());
            if (user != null) {
                JOptionPane.showMessageDialog(this, "Login sucessfully");
                if (user.getRole().equals("Cán bộ")) {
                    this.dispose();
                    new QuanLySinhVien().setVisible(true);
                }
                if(user.getRole().equals("Giảng viên")){
                    this.dispose();
                    new QuanLyDiemSinhVien().setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Login failed");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

