    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        try {
            //BÆ°á»c 1: Táº¡o Äá»i tÆ°á»£ng Properties vÃ  chá» Äá»nh thÃ´ng tin host, port
            //Nhá» import java.util.Properties; copy nguyÃªn code cá»§a Lab
            
            Properties p = new Properties();
            p.put("mail.smtp.auth", "true");
            p.put("mail.smtp.starttls.enable", "true");
            p.put("mail.smtp.host", "smtp.gmail.com");
            p.put("mail.smtp.port", 587);
            //BÆ°á»c 2: Táº¡o Äá»i tÆ°á»£ng Session (phiÃªn lÃ m viá»c)            
            //Nhá» import javax.mail.Session;
            //Nhá» import java.net.PasswordAuthentication;
            String userName= tfUserName.getText();
            String password = new String(pwPassword.getPassword());
            Session s = Session.getInstance(p,
                    new javax.mail.Authenticator() {
                        protected PasswordAuthentication getPasswordAuthentication(){
                            return new PasswordAuthentication(userName, password);
                        }
            }
            );
            //BÆ°á»c 3: Táº¡o Äá»i tÆ°á»£ng messeage
            //nhhá» import javax.mail.Message;
            String from = tfUserName.getText();
            String to = tfTo.getText();
            String subject = tfSubject.getText();
            String body= taMessage.getText();    
            String cc=tfCC.getText();
            String bcc= tfBCC.getText();
            Message message = new MimeMessage(s);
            message.setFrom(new InternetAddress(from));            
            message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(to));
            message.setRecipients(Message.RecipientType.CC, InternetAddress.parse(cc));
            message.setRecipients(Message.RecipientType.BCC, InternetAddress.parse(bcc));
            message.setSubject(subject);
            message.setText(body);

            //BÆ°á»c 4: Gá»­i mail
            Transport.send(message);
            JOptionPane.showMessageDialog(null, "Gá»­i thÆ° thÃ nh cÃ´ng!");
        } catch (Exception ex) {
            System.out.println("Gáº·p lá»i:" + ex);
        }
    }//GEN-LAST:event_btnSendActionPerformed

